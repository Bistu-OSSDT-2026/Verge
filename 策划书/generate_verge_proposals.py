def set_cell_shading(cell, color):
    """设置单元格背景色"""
    shading = OxmlElement('w:shd')
    shading.set(qn('w:fill'), color)
    cell._tc.get_or_add_tcPr().append(shading)

def create_heading_style(doc, style_name, font_name, font_size, bold=True, color=None, space_before=12, space_after=6):
    """创建标题样式"""
    try:
        style = doc.styles.add_style(style_name, WD_STYLE_TYPE.PARAGRAPH)
    except:
        style = doc.styles[style_name]
    
    font = style.font
    font.name = font_name
    font.size = Pt(font_size)
    font.bold = bold
    if color:
        font.color.rgb = RGBColor.from_string(color)
    
    # 设置中文字体
    style._element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
    
    pf = style.paragraph_format
    pf.space_before = Pt(space_before)
    pf.space_after = Pt(space_after)
    
    return style

def add_formatted_paragraph(doc, text, style='Normal', bold=False, color=None, align=None, space_after=6):
    """添加格式化段落"""
    p = doc.add_paragraph(text, style=style)
    
    if bold or color:
        for run in p.runs:
            if bold:
                run.bold = True
            if color:
                run.font.color.rgb = RGBColor.from_string(color)
    
    if align:
        p.alignment = align
    
    p.paragraph_format.space_after = Pt(space_after)
    return p

def create_table(doc, headers, rows, col_widths=None):
    """创建格式化表格"""
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    
    # 表头
    header_cells = table.rows[0].cells
    for i, header in enumerate(headers):
        header_cells[i].text = header
        header_cells[i].paragraphs[0].runs[0].bold = True
        set_cell_shading(header_cells[i], 'D9E2F3')  # 浅蓝色背景
    
    # 数据行
    for row_idx, row_data in enumerate(rows):
        row_cells = table.rows[row_idx + 1].cells
        for col_idx, cell_data in enumerate(row_data):
            row_cells[col_idx].text = str(cell_data)
    
    # 设置列宽
    if col_widths:
        for i, width in enumerate(col_widths):
            for cell in table.columns[i].cells:
                cell.width = Cm(width)
    
    return table

